const myName = 'Danny'
console.log("hi my name is %s", myName) // hi my name is Danny

const myAge = 26
console.log("hi my age is %d", myAge) // hi my age is 26

const myAge2 = 3.14
console.log("hi my age is %f", myAge2) // hi my age is 3.14

const myInfo = { age: 26, name: 'Danny' }
console.log("hi my info is %o", myInfo) // hi my info is { age: 26, name: 'Danny' }

console.log("hi %d %s Rocks", 5, "Typed", "Something") // hi 5 Typed Rocks Something
// %d -> 5
// %s -> Typed

// %d -> number type variable
// %s -> string type variable
// %f -> float number type variable
// %f -> object hyperlink